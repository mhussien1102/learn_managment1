class ProductModel {
  final int id;
  final String name;
  final String desc;
  final String author;
  final String category;
  final int price;
  final int maxQuantity;
  final int sales;
  final String url;

  ProductModel({
    required this.id,
    required this.name,
    required this.desc,
    required this.author,
    required this.category,
    required this.price,
    required this.maxQuantity,
    required this.sales,
    required this.url,
  });
}
